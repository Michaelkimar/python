import math
def get_sine(x):
    angle_radians=math.radians(x)
    angles_in=math.sin(angle_radians)
    return angles_in
def get_tangent(x):
    angle_radians=math.radians(x)
    angles_in=math.tan(angle_radians)
    return angles_in    
def get_cosine(x):
    angle_radians=math.radians(x)
    angles_in=math.cos(angle_radians)
    return angles_in    

